    <h2 class="sub-heading"><br><br>A basic question but kind of important too, don’t you think?</h2>
    <p class="post-text"></p>Long ago, this process of finding a family with whom to place a child was mysterious. 
    But now thanks to technology, prospective birthmothers can research prospective adoptive families on the internet and through
    “look books” that give a snapshot in pictures of the family’s life together. Some families will consist of couples with cats or dogs.
    Other families will already have children. It’s great to have options, but it may lead to the question of why people adopt in the first place.<br><br>
    Adoption happens for lots of reasons. Some people knew at a very young age they wanted to adopt a child someday. Others have said they feel ‘called’ 
    to take in one or more children to their existing family structure. Each family situation is different, but so is each unplanned pregnancy.<br>
    To give you more understanding, here are four reasons families choose to adopt:<br><br>
    <ul>
        <li>Infertility. About 1 in 8 couples in American cannot get pregnant due to some sort of infertility. If that couple has the desire for children,
          they have a few options including adoption. Depending on their particular situation, many couples will pursue medical interventions such as in 
          vitro fertilization (IVF) when they are unable to conceive on their own. And for some, medical interventions will not work and they will still 
          be unable to conceive. Fostering, adoption and surrogacy are the most prevalent choices for those couples.<br><br></li>
        <li>Medical Condition. Not all women who are able to become pregnant should become pregnant and carry a baby to term. There are a wide range of medical
          conditions that would make a woman’s body unsafe and or put her health at risk for a safe 40-week pregnancy. So those couples are looking at some 
          kind of fostering or adopting or surrogacy.<br><br></li>
        <li>Single. Single parenting is on the rise with the breakdown of the traditional family. Single people who are stable and desire to raise a child have 
          a couple of options, one of which is adoption. Adoption laws have come a long way from the “must be under 40 and married for at least 2 years before
          adopting” way things used to be.<br><br></li>
        <li>Called to grow their existing family. Many families have always loved the idea of adoption and have biological children while also adopting. 
          This is a great way for them to expand their family without bearing more children. The orphan care movement happening in our country today is 
          contributing to the growth of many families through adoption.<br><br></li>
      </ul>
      <p class="post-text"></p>There can also be further discussion about domestic versus foreign adoptions including wait times, financial burdens and older children versus babies. 
      Adoption can be a joy to all involved, but that joy often follows a time of waiting by parties on both sides
      
</section>

